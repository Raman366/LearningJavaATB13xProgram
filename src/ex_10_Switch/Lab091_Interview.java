package ex_10_Switch;

public class Lab091_Interview {
    public static void main(String[] args) {
        char code = 'C';

        switch (code){
            default:
                System.out.println("Hellooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;

        }
    }
}
