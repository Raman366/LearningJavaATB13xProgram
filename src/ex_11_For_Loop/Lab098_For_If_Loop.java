package ex_11_For_Loop;

public class Lab098_For_If_Loop {
    public static void main(String[] args) {
        for (int raman =0;raman<18; raman++){
            // 0 to 17, 18 times
            if (raman > 15){
                System.out.println("He will get an iPhone!");
            }else {
                System.out.println("He will get candy!");
            }
        }
    }
}
